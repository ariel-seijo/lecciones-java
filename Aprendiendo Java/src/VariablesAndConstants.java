public class VariablesAndConstants {
    public static void main(String[] args) {

        // variables

        String name = "Arielo";
        String space = " ";
        String surname = "Seijo";

        System.out.println(name + space + surname);

        // reasigno variable

        name = "Arielovich";

        System.out.println(name + space + surname);

        // otra forma de crear variables sin declarar tipo de dato

        var name2 = "Arielovich";

        System.out.println(name2);

        // constantes

        final String email = "arielovich@email.com";

        System.out.println(email);

    }
}
